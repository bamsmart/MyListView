package learning.shinesdev.mylistview;

public class ShioModel {
    // tambahkan atribut untuk shio
    String name;
    String desc;
    int image;

    // tambahkan setter dan getter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
